private void listarDados() {
        try {
            ClienteDAO objClienteDAO = new ClienteDAO();
            VeiculoDAO objVeiculoDAO = new VeiculoDAO();
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setNumRows(0);

            ArrayList<ClienteDAO> listaCliente = objClienteDAO.listaCliente();
            ArrayList<VeiculoDAO> listaVeiculo = objVeiculoDAO.listaVeiculo();
            System.out.println("Total de clientes" + listaCliente.size());

            for (int i = 0; i < listaVeiculo.size(); i++) {
                model.addRow(new Object[]{
                    listaCliente.get(i).getNome(),
                    listaVeiculo.get(i).getMarca(),
                    listaVeiculo.get(i).getModelo(),
                    listaVeiculo.get(i).getPlaca()
                });
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao Listar Clientes" + erro.getMessage());
        }
    }

    private void excluirDados() {
        String nome = jTextField1.getText();

        ClienteDAO objClienteDAO = new ClienteDAO();
        objClienteDAO.setNome(nome);

        objClienteDAO.removerCliente(objClienteDAO);
    }

    private void selecionarDados() {
        int setar = jTable2.getSelectedRow();

        // SaÃ­da de dados
        jTextField1.setText(jTable2.getModel().getValueAt(setar, 0).toString());
    }

